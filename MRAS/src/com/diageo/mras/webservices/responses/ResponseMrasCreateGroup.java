package com.diageo.mras.webservices.responses;
import java.util.List;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class ResponseMrasCreateGroup {
	private int groupId;
	private String responseMessage;
	private List<String> invalidPhoneNumber;
	private List<Integer> invalidConsumerId;

	public int getGroupId() {
		return groupId;
	}

	public void setGroupId(int groupId) {
		this.groupId = groupId;
	}

	public List<String> getInvalidPhoneNumber() {
		return invalidPhoneNumber;
	}

	public void setInvalidPhoneNumber(List<String> invalidPhoneNumber) {
		this.invalidPhoneNumber = invalidPhoneNumber;
	}

	public List<Integer> getInvalidConsumerId() {
		return invalidConsumerId;
	}

	public void setInvalidConsumerId(List<Integer> invalidConsumerId) {
		this.invalidConsumerId = invalidConsumerId;
	}

	public String getResponseMessage() {
		return responseMessage;
	}

	public void setResponseMessage(String responseMessage) {
		this.responseMessage = responseMessage;
	}

}
